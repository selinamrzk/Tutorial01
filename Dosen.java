
public class Dosen {
	String nama;
	String nik;
	
	public Dosen(String nama, String nik) {
		super();
		this.nama = nama;
		this.nik = nik;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getNik() {
		return nik;
	}
	public void setNpm(String nik) {
		this.nik = nik;
	}
}